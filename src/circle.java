public class circle {
    public static void main(String[] args) {
        Double area,circumference;
        Double PI=3.14;
        Double r=7.5;
        //compute area and circumference of circle
        area=PI*r*r;
        circumference=2*PI*r;
        System.out.println("Area= " +area+"\n"+"circumference= " +circumference);
    }
}
